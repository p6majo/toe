package com.p6majo.plot;

public class PlotType {
    private Boolean phaseColored = false;
    private Boolean modulusContour = false;


    public Boolean getPhaseColored() {
        return phaseColored;
    }

    public void setPhaseColored(Boolean phaseColored) {
        this.phaseColored = phaseColored;
    }

    public Boolean getModulusContour() {
        return modulusContour;
    }

    public void setModulusContour(Boolean modulusContour) {
        this.modulusContour = modulusContour;
    }
}
